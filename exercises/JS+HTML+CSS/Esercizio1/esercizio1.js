function cambiaColore() {
  // il testo bianco è il nome della funzione, poi nelle graffe c'è il contenuto della funzione //
  var paragrafo = document.querySelector("p"); //Prendi una variabile e selezionala tramite un selector di documenti, non scordare il parametro//
  paragrafo.style.color = "red"; // Imposta il colore del testo del paragrafo su rosso //
}
